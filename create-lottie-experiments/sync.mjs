#!/usr/bin/env node
// Regenerates ./template from the repo root so the published package bundles a
// current snapshot of the app. The repo root is the single source of truth; the
// template is derived. Runs automatically on `npm pack`/`npm publish` (prepack)
// and can be run manually:  node create-lottie-experiments/sync.mjs
import { cp, rm, rename, readdir, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, "..");
const template = path.join(here, "template");

// Top-level entries (repo-root children) never copied into the template.
const SKIP_TOP = new Set([
  "node_modules",
  "dist",
  "dist-ssr",
  ".git",
  "bun.lock",
  "create-lottie-experiments", // this package itself (avoids self-recursion)
]);

// Nested paths (relative to the repo root) to drop while copying.
const SKIP_NESTED = new Set([
  path.join("public", "canvaskit.wasm"), // regenerated by postinstall
  path.join(".claude", "settings.local.json"), // user-local settings
]);

const rel = (p) => path.relative(root, p);

await rm(template, { recursive: true, force: true });
await mkdir(template, { recursive: true });

// Copy each top-level entry individually — fs.cp refuses to copy a directory
// into a subdirectory of itself, so we can't copy the root wholesale.
for (const entry of await readdir(root)) {
  if (SKIP_TOP.has(entry)) continue;
  await cp(path.join(root, entry), path.join(template, entry), {
    recursive: true,
    filter: (src) => {
      const r = rel(src);
      if (path.basename(r) === ".DS_Store") return false;
      return !SKIP_NESTED.has(r);
    },
  });
}

// npm strips/renames a literal `.gitignore` inside published packages, so ship
// it as `_gitignore`; the CLI restores the name when scaffolding.
const gitignore = path.join(template, ".gitignore");
if (existsSync(gitignore)) {
  await rename(gitignore, path.join(template, "_gitignore"));
}

console.log(`Synced template <- repo root  (${template})`);
